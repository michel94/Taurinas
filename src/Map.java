	BufferedImage[] blocks = new BufferedImage[3];
	
	public Map(){
		
		for (int i = 0 ; i < blocks.length; i++) {
			try{
				blocks[i] = ImageIO.read(new File("../Img/Map/" + i + ".png"));
			}catch (IOException e) {}
		}
	}
	
	public void paint(Graphics g, Coordinates playerPos, Dimension screen){
		int x = (int)(playerPos.x);
		int y = (int)(playerPos.y);
		double restoX = (playerPos.x%1);
		double restoY = (playerPos.y%1);
		
		int tempI, tempJ;
		int pixelX, pixelY;
		
		for(int i = y-8; i < y + 8; i++){
			for (int j = x-15;  j < x + 15; j++) {
				
				if(i < 0) tempI = i+matrix.length;
				else if(i >= matrix.length) tempI = i-matrix.length;
				else tempI = i;
				
				if(j < 0) tempJ = j+matrix[0].length;
				else if(j >= matrix[0].length) tempJ = j-matrix[0].length;
				else tempJ = j;
				
				
				pixelX = screen.width/2 - 15*50 + (int)((j-x+15)*50 - restoX * 50);
				pixelY = screen.height/2 - 8*50 + (int)((i-y+8)*50 - restoY * 50);				
				
				g.drawImage(blocks[matrix[tempI][tempJ]], pixelX, pixelY, null);
			}
		}
	}
	
	public int getMatrixVal(int x, int y){
		if(x < 0) x += matrix[0].length;
		else if(x >= matrix[0].length) x -= matrix[0].length;
		
		if(y < 0) y += matrix.length;
		else if(y >= matrix.length) y -= matrix.length;
		
		return matrix[y][x];
	}
	
